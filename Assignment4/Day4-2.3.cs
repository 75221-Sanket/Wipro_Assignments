using System;

class Series3
{
    static void Main()
    {
        Console.WriteLine("Series");

        int[] series = { 3, 5, 6, 7, 10, 12, 15, 18, 20, 21, 24, 25, 27, 30 };

        for (int i = 0; i < series.Length; i++)
        {
            Console.WriteLine(series[i] + "");

        }
        Console.WriteLine();
    }
}